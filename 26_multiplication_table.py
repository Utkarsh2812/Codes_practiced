num = int(input('Enter the Number:  '))
for k in range(11 , 1):
    print(str(num) + ' × ' + str(k) + " = " + str( num * k ))

print('Done 👍')

# Using While

num = int(input('Enter the Number:  '))
a = 1

while a<11:
   print(str(num) + ' × ' + str(a) + " = " + str( num * a ))
   a = a+1

print('Done 👍') 